    private void btnTambahDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahDataActionPerformed
        // TODO add your handling code here:
        try {
            Class.forName("com.mysql.jdbc.Driver").newInstance();
            final String username = "root";
            final String password = "";
            final Connection koneksi = DriverManager.getConnection(
                    "jdbc:mysql://localhost:3306/latihan_db", username, password);
            
            System.out.println("Koneksi Berhasil");
            
            String input = "INSERT INTO biodata_mhs (nim,nama,jenis_kelamin,"
                    + "tanggal_lahir,alamat,fakultas,jurusan,angkatan)"
                    + "VALUES (?,?,?,?,?,?,?,?)";
            
            PreparedStatement inputdata = koneksi.prepareStatement(input);
            inputdata.setString(1, txtNIM.getText().trim());
            inputdata.setString(2, txtNAMA.getText().trim());
            String jeniskelamin = null;
            if (rbL.isSelected()){
                jeniskelamin = rbL.getText();
            } else {
                jeniskelamin = rbP.getText();
            }
            inputdata.setString(3, jeniskelamin.trim());
            inputdata.setString(4, txtTANGGALLAHIR.getText().trim());
            inputdata.setString(5, txtALAMAT.getText().trim());
            inputdata.setString(6, txtFAKULTAS.getText().trim());
            inputdata.setString(7, txtJURUSAN.getText().trim());
            inputdata.setString(8, txtANGKATAN.getText().trim());
            inputdata.execute();
            JOptionPane.showMessageDialog(null,"Data Berhasil di Simpan.", "PEMBERITAHUAN", JOptionPane.INFORMATION_MESSAGE);
            
        } catch (final SQLException | InstantiationException | IllegalAccessException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null,"Data Gagal di Simpan.", "PEMBERITAHUAN", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnTambahDataActionPerformed

