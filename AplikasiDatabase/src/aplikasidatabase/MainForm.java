    private void btnUbahDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahDataActionPerformed
        // TODO add your handling code here:
        try {
            Class.forName("com.mysql.jdbc.Driver").newInstance();
            final String username = "root";
            final String password = "";
            final Connection koneksi = DriverManager.getConnection(
                    "jdbc:mysql://localhost:3306/latihan_db", username, password);
            
            System.out.println("Koneksi Berhasil");
            
            String NIM = txtNIM.getText();
            String NAMA = txtNAMA.getText();
            String jeniskelamin = null;
            if (rbL.isSelected()){
                jeniskelamin = rbL.getText();
            } else {
                jeniskelamin = rbP.getText();
            }
            String JENISKELAMIN = jeniskelamin;
            String TANGGALLAHIR = txtTANGGALLAHIR.getText();
            String ALAMAT = txtALAMAT.getText();
            String FAKULTAS = txtFAKULTAS.getText();
            String JURUSAN = txtJURUSAN.getText();
            String ANGKATAN = txtANGKATAN.getText();
            if(NIM.length()==9){
                String edit = "UPDATE biodata_mhs SET nim='"+NIM+"',"
                        + "nama='"+NAMA+"',"
                        + "jenis_kelamin='"+jeniskelamin+"',"
                        + "tanggal_lahir='"+TANGGALLAHIR+"',"
                        + "alamat='"+ALAMAT+"',"
                        + "fakultas='"+FAKULTAS+"',"
                        + "jurusan='"+JURUSAN+"',"
                        + "angkatan='"+ANGKATAN+"' WHERE nim='"+NIM+"'";
                PreparedStatement editdata = koneksi.prepareStatement(edit);
                editdata.execute();
                JOptionPane.showMessageDialog(null,"Data Berhasil di Ubah.", "PEMBERITAHUAN", JOptionPane.INFORMATION_MESSAGE);
            } else if (NIM.isEmpty())
                JOptionPane.showMessageDialog(null,"NIM tidak boleh kosong.", "PEMBERITAHUAN", JOptionPane.ERROR_MESSAGE);
            else {
                JOptionPane.showMessageDialog(null,"NIM Anda Salah.", "PEMBERITAHUAN", JOptionPane.ERROR_MESSAGE);
            }
        } catch (final SQLException | InstantiationException | IllegalAccessException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null,"Data Gagal di Ubah.", "PEMBERITAHUAN", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUbahDataActionPerformed

